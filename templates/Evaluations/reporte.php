    <h2>MODELO KEMPEM</h2>
    <p>
    Las competencias son repertorios de comportamientos que algunas personas
    dominan mejor que otras, lo que las hace eficientes en una situación
    determinada. Considerando que el emprendimiento se traduce en
    comportamientos, el enfoque basado en competencias expresa con mayor
    precisión aquellos atributos necesarios en la generación de tales
    comportamientos. ¿Qué es la competencia emprendedora? ¿Cuáles son sus
    dimensiones? ¿Cuáles son sus atributos?</p>
    <p>
    El Modelo KEMPEM es una metodología validada y automatizada que permite
    medir y analizar los 20 atributos de la competencia emprendedora agrupados
    en 3 dimensiones: a) conocimientos, b) habilidades y, c) actitudes y valores).
    Además, muestra el nivel de madurez del potencial emprendedor de las
    personas. La validez y fiabilidad de la metodología ha sido probada en varios
    grupos de interés: emprendedores, empleados, gerentes y estudiantes
    universitarios, de varios países, incluso, se han realizado algunas mediciones
    en adolescentes.</p>
    <p>
    Evaluamos los factores que condicionan la acción emprendedora en los
    individuos, así como las formas efectivas de estimularlos. Es por ello que, con
    una investigación iniciada en el 2009, hemos definido, medido, analizado y
    validado los atributos del emprendedor y el valor de éstos en la creación,
    crecimiento y fortalecimiento empresarial, ello nos llevó a desarrollar
    metodologías, modelos teóricos y estadísticos de análisis, algoritmos e
    instrumentos propios que permiten clasificar, jerarquizar y caracterizar el
    perfil emprendedor. Nuestro abordaje es multivariado y está orientado a
    evaluar sin sesgos el desempeño actual y a desarrollar análisis predictivos del
    desempeño futuro de la persona, mediante la mejora continua del modelo, el
    cual a medida que se ingresan más datos, se hace más efectivo y eficiente.</p>
    <p>
    El modelo tiene un 95% de confianza para todos sus ítems y una precisión en
    la caracterización de madurez del potencial emprendedor superior al 97%.
    Adicionalmente, los resultados obtenidos por usted son comparados con una
    base de datos propia constituida por más de 7.200 personas que ya
    participaron en este estudio.</p>
